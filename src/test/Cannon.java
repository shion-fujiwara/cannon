package test;

public class Cannon {

	private double dex;
	private int count;


	public Cannon(double dex, int count) {
		this.dex = dex;
		this.count = count;
	}

	public double getDex() {
		return dex;
	}
	public void setDex(double dex) {
		this.dex = dex;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}

}
